/*
Find suitable types for variables. You are given the following types:
byte, short, int, long, char, boolean, float, double, and String.
Assign the following values to them false, “Palo Alto, CA”, 32767, 2000000000, 0.1234567891011, 0.5f, 919827112351L, 127, ‘c’.
Try to assign 32768 to short and see what happens.
*/

public class _03_AssignVariables {
    public static void main(String[] args) {

        byte firstValue = 127;
        short secondValue = 32767;
        int thirdValue = 2000000000;
        long fourthValue = 919827112351L;
        char fifthValue = 'c';
        boolean sixthValue = false;
        float seventhValue = 0.5f;
        double eighthValue = 0.1234567891011;
        String ninthValue = "Palo Alto, CA";
    }
}
